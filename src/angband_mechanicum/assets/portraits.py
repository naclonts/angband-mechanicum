def get_portrait(portrait_id: str) -> str:
    """Return the art string for a portrait by its id.

    Raises ``KeyError`` if the id is not found.
    """
    for p in PORTRAITS:
        if p["id"] == portrait_id:
            return p["art"]
    raise KeyError(f"No portrait with id '{portrait_id}'")


def random_portrait() -> dict[str, str]:
    """Return a random portrait dict (id, name, art)."""
    return _random.choice(PORTRAITS)
